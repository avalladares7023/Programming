create table MAJOR(
	MAJOR_NUM int not null,
	MAJOR_NAME varchar(30) not null,
	ABET_ACCR varchar(3),
	primary key (MAJOR_NUM)
);

create table PROFESSION(
	PROF_NUM int not null,
	PROF_NAME varchar(60) not null, 
	PROF_DEG varchar(30),
	PROF_EWE varchar(30),
	PROF_JOB int,
	MAJOR_NUM int,
	foreign key (MAJOR_NUM) references MAJOR(MAJOR_NUM),
	primary key (PROF_NUM, MAJOR_NUM)
);

create table LOCATION(
	LOC_NUM bigint(20) not null,
	LOC_STATE varchar(20),
	LOC_EMP int,
	LOC_HMW double,
	LOC_AMW int, 
	PROF_NUM int,
	primary key (LOC_NUM),
	foreign key (PROF_NUM) references PROFESSION(PROF_NUM)
);

create table CONCENTRATION(
	CON_NUM int not null,
	CON_NAME varchar(30) not null,
	MAJOR_NUM int,
	primary key (CON_NUM),
	foreign key (MAJOR_NUM) references MAJOR(MAJOR_NUM)
);

create table STUDENT(
	STU_NUM bigint(20) not null,
	STU_FNAME varchar(30) not null,
	STU_LNAME varchar(30) not null,
	MAJOR_NUM int,
	CON_NUM int,
	primary key (STU_NUM),
	foreign key (MAJOR_NUM) references MAJOR(MAJOR_NUM),
	foreign key (CON_NUM) references CONCENTRATION(CON_NUM)
);

create table WAGE(
	WAGE_NUM bigint(20) not null,
	WAGE_MEDH double, 
	WAGE_MEDA int,
	WAGE_IND varchar(60),
	PROF_NUM int,
	primary key (WAGE_NUM),
	foreign key (PROF_NUM) references PROFESSION(PROF_NUM)
);









